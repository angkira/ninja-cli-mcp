def add(a: float, b: float) -> float:
    """Add two numbers and return the result.

    Args:
        a: First number
        b: Second number

    Returns:
        The sum of a and b
    """
    return a + b


def subtract(a: float, b: float) -> float:
    """Subtract b from a and return the result.

    Args:
        a: First number
        b: Second number

    Returns:
        The difference of a and b
    """
    return a - b
